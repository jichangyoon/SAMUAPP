export function getCoordinates(country: string, city?: string): { lat: number; lng: number } | null {
  if (city) {
    const cityKey = city.toLowerCase().trim();
    if (cityCoordinates[cityKey]) {
      return cityCoordinates[cityKey];
    }
  }

  const countryKey = country.toUpperCase().trim();
  if (countryCoordinates[countryKey]) {
    return { lat: countryCoordinates[countryKey].lat, lng: countryCoordinates[countryKey].lng };
  }

  return null;
}

export function getCountryName(code: string): string {
  const countryKey = code.toUpperCase().trim();
  return countryCoordinates[countryKey]?.name || code;
}
